package com.affablebean.entity1;

public class MsgSubject {

	private final Long id;
	private final String name;

	public MsgSubject(Long id, String name) {
		this.id = id;
		this.name = name;
	}

	public Long getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	@Override
	public String toString() {
		return "MsgSubject{" + "id=" + id + ", name=" + name + '}';
	}

}
